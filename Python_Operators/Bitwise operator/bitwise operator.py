# bitwise AND (&) operator
x=15
y=17
print(x,"==",bin(x))
print(y,"==",bin(y))
print("bitwise And (&) operator",x & y)

#bitwise OR (|) operator
print("----------------------------")
x1=9
y1=10
print(x1,"==",bin(x1))
print(y1,"==",bin(y1))
print("bitwise OR (|) operator",x1|y1)

#bitwise xor (^)m operator
print("******************************************")
x1=9
y1=10
print(x1,"==",bin(x1))
print(y1,"==",bin(y1))

print("bitwise xor (^)m operator ",x1^y1)
print("--------------------------")
#bitwise left shift(<<) operator
r=3<<2
print("bitwise left shift(<<) operator 3<<2",r)
#bitwise right shift(>>) operator
res=10>>2
print("bitwise right shift(>>) operator 10>>2",res)

