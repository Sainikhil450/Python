# And Operator

# And Operator :-it compares if all conditional statement is true
# output is true
# if they one of the statement is false,then final output also False

a=10
b=100
c=120
res=(a<b)and(b<c)
print("And operator:-",res)

print("******************************************************")

# OR operators
# Or operator :- it check all conditional statement
# any one condition is true means final output also True
x=100
y=230
z=50
res1=(x>y)or(y>z)
print("or operator:-",res1)

print("******************************************************")
# Not Operator
# it is reverse
# not(true)-->false
# not(false)-->True
res2=(100>50)or(50!=25)
print("not operator:-",not(res2))
