# equal to --> it compares two values equal or not
# equal -->True
# not equal -->False
a=10
b=10
s=a==b
print("equal to a=10,b=10:-",s)
print("***********************************************")
# not equal to--> it compares two values equal or not
# equal -->false
# not equal-->True
x=10
y=15
s1=x!=y
print("not equal to x=10,y=15:-",s1)
print("***********************************************")
# Greater than--> it compares two values left hand side is greater R.H.S>True
# 10 >6-->True
# 15 >20-->False
x=10
y=6
s1=x>y
print("greater than x=10,y=6:",s1)
print("***********************************************")
# less than--> it compares two values left hand side is greater R.H.S<True
# 6<5-->True
# 10<5-->False
x=5
